#include <iostream>
using namespace std;
int main()
{
	int first_num, second_num;
	
	cout << "Input 1st number: "; cin >> first_num;
	cout << "Enter 2nd number: "; cin >> second_num;
	
	cout << "After swapping the 1st number is: " << second_num;
	cout << endl;
	cout << "After swapping the 2nd number is: " << first_num;
	return 0;
}
